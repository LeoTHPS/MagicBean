local function MagicBean_InitThread(process, handle)
	local thread = {};

	thread.Handle = handle;

	setmetatable(thread, {
		__gc = function(self)
			magic_bean_process_close_thread(process, self.Handle);
		end
	});

	function thread:IsRunning()
		return magic_bean_thread_is_running(self.Handle) and true or false;
	end

	function thread:GetId()
		local value = magic_bean_thread_get_id(self.Handle);

		return tonumber(value);
	end

	function thread:Resume()
		return magic_bean_thread_resume(self.Handle) and true or false;
	end
	function thread:Suspend()
		return magic_bean_thread_suspend(self.Handle) and true or false;
	end

	-- @param timeout_ms is optional
	-- @return 0 on error
	-- @return -1 on timeout
	function thread:Join(timeout_ms)
		if not timeout_ms then
			timeout_ms = MagicBean.INFINITE;
		end

		local value = magic_bean_thread_join(self.Handle, timeout_ms);

		return tonumber(value);
	end

	function thread:Terminate(exit_code)
		return magic_bean_thread_terminate(self.Handle, exit_code) and true or false;
	end

	return thread;
end
local function MagicBean_InitWindow(process, handle)
	local window = {};

	window.Handle = handle;

	setmetatable(window, {
		__gc = function(self)
			magic_bean_process_close_window(process, self.Handle);
		end
	});

	function window:IsFocus()
		return magic_bean_window_is_focus(self.Handle) and true or false;
	end

	function window:GetTitle()
		local value = magic_bean_window_get_title(self.Handle);

		return tostring(value);
	end

	-- @param state is optional
	function window:SendKey(value, state)
		if state ~= nil then
			return magic_bean_window_send_key(self.Handle, value, state) and true or false;
		end

		if not magic_bean_window_send_key(self.Handle, value, MagicBean.KEY_STATE_DOWN) then
			return false;
		end

		if not magic_bean_window_send_key(self.Handle, value, MagicBean.KEY_STATE_UP) then
			return false;
		end

		return true;
	end
	-- @param state is optional
	function window:SendButton(value, x, y, state)
		if state ~= nil then
			return magic_bean_window_send_button(self.Handle, value, state, x, y) and true or false;
		end

		if not magic_bean_window_send_button(self.Handle, value, MagicBean.BUTTON_STATE_DOWN, x, y) then
			return false;
		end

		if not magic_bean_window_send_button(self.Handle, value, MagicBean.BUTTON_STATE_UP, x, y) then
			return false;
		end

		return true;
	end
	function window:SendString(value)
		return magic_bean_window_send_string(self.Handle, value) and true or false;
	end

	function window:SetTitle(value)
		return magic_bean_window_set_title(self.Handle, value) and true or false;
	end

	return window;
end
